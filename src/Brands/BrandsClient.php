<?php

namespace Courier\Brands;

use GuzzleHttp\ClientInterface;
use Courier\Core\Client\RawClient;
use Courier\Brands\Types\BrandParameters;
use Courier\Brands\Types\Brand;
use Courier\Exceptions\CourierException;
